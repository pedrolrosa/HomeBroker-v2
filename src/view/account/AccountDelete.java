    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if(AccountController.delete(Long.valueOf(idComboBox.getSelectedItem().toString()))){
            JOptionPane.showMessageDialog(this,"Deleted Sucess!");
            List<Account> accounts;
            accounts = AccountController.read();

            for(Account account : accounts){

                idComboBox.addItem(String.valueOf(account.getId()));
            }

            idComboBox.removeAllItems();
            listItems();
        } else {
            JOptionPane.showMessageDialog(this,"Failed !");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

