    public OrderExecutionRead() {
        initComponents();
        
        DefaultTableModel model = (DefaultTableModel) readTable.getModel();
        model.setNumRows(0);
        
        Object columns[] = new Object[readTable.getColumnCount()];
            
        List<OrderExecution> executions;
        executions = OrderExecutionController.read();
        
        for(int i =0; i < executions.size(); i++){
            
            OrderExecution execution = executions.get(i);
            
            columns[0] = execution.getId();
            columns[1] = execution.getOrder();
            columns[2] = execution.getBuyer();
            columns[3] = execution.getSeller();
            columns[4] = execution.getQuantity();
            columns[5] = execution.getStart();
            columns[6] = execution.getModify();
            
            model.addRow(columns);
        }
    }

